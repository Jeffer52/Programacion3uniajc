import java.util.Queue;
import java.util.ArrayDeque;

public class CompararColas {

    // Método que compara dos colas de Integer y devuelve true si son idénticas
    public static boolean sonColasIdenticas(Queue<Integer> cola1, Queue<Integer> cola2) {
        // Si las colas tienen tamaños diferentes, no pueden ser idénticas
        if (cola1.size() != cola2.size()) {
            return false; // Retorna false si el tamaño es distinto
        }

        // Crear copias de las colas originales para no modificarlas durante la comparación
        Queue<Integer> copiaCola1 = new ArrayDeque<>(cola1);  // Copia de cola1
        Queue<Integer> copiaCola2 = new ArrayDeque<>(cola2);  // Copia de cola2

        // Se comparan elemento por elemento mientras las copias no estén vacías
        while (!copiaCola1.isEmpty()) {
            // Se saca el primer elemento de cada copia
            Integer elemento1 = copiaCola1.poll();  // Extrae y remueve el primer elemento de copiaCola1
            Integer elemento2 = copiaCola2.poll();  // Extrae y remueve el primer elemento de copiaCola2

            // Si los elementos extraídos no son iguales, las colas no son idénticas
            if (!elemento1.equals(elemento2)) {
                return false;  // Retorna false en cuanto se detecta diferencia
            }
        }

        // Si se han comparado todos los elementos sin encontrar diferencias, las colas son idénticas
        return true;
    }

    public static void main(String[] args) {
        // Creación de la primera cola e inserción de elementos
        Queue<Integer> colaA = new ArrayDeque<>();
        colaA.add(1);  // Agrega el elemento 1
        colaA.add(2);  // Agrega el elemento 2
        colaA.add(3);  // Agrega el elemento 3
        colaA.add(4);  // Agrega el elemento 4
        
        // Creación de la segunda cola idéntica a la primera
        Queue<Integer> colaB = new ArrayDeque<>();
        colaB.add(1);
        colaB.add(2);
        colaB.add(3);
        colaB.add(4);
        
        // Creación de una tercera cola con un elemento diferente
        Queue<Integer> colaC = new ArrayDeque<>();
        colaC.add(1);
        colaC.add(2);
        colaC.add(3);
        colaC.add(5);  // Elemento diferente para probar la desigualdad

        // Pruebas de comparación
        System.out.println(sonColasIdenticas(colaA, colaB)); // Imprime true, ya que son idénticas
        System.out.println(sonColasIdenticas(colaA, colaC)); // Imprime false, ya que difieren en el último elemento
    }
}